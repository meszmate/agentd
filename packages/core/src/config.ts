/**
 * Auto-discover available codex models from the cache that the
 * codex CLI itself maintains at `~/.codex/models_cache.json`. Codex
 * refreshes this file on its own, so reading it gives us a list
 * that's always current (no agentd registry edits when openai
 * ships a new version). Returns the visible entries sorted by
 * priority. Empty array on any error (missing file, bad JSON, etc.).
 */
export function loadCodexModelsFromCache(): ModelEntry[] {
  return loadCodexCache().models;
}

/**
 * Same source data as `loadCodexModelsFromCache` but also returns the
 * cache's `fetched_at` timestamp so callers can surface staleness in
 * the UI ("model list pulled from codex 3 min ago"). When the file is
 * missing or unreadable the timestamp is null.
 */
export interface CodexCache {
  models: ModelEntry[];
  fetchedAt: number | null;
}

export function loadCodexCache(): CodexCache {
  const path = join(homedir(), ".codex", "models_cache.json");
  if (!existsSync(path)) return { models: [], fetchedAt: null };
  try {
    interface CachedModel {
      slug?: string;
      display_name?: string;
      description?: string;
      visibility?: string;
      priority?: number;
    }
    const data = JSON.parse(readFileSync(path, "utf8")) as {
      models?: CachedModel[];
      fetched_at?: string;
    };
    const fetchedAt = data.fetched_at
      ? Date.parse(data.fetched_at) || null
      : null;
    const models = data.models ?? [];
    const visible = models
      .filter(
        (m) =>
          typeof m.slug === "string" &&
          (m.visibility === "list" || !m.visibility),
      )
      .sort((a, b) => (a.priority ?? 999) - (b.priority ?? 999));
    return {
      models: visible.map((m): ModelEntry => {
        const slug = m.slug!;
        const lower = slug.toLowerCase();
        // Best-effort tier guess from the slug. Operators can override
        // via cfg.models.codex in config.json if they care.
        const tier: "fast" | "balanced" | "deep" = lower.includes("mini")
          ? "fast"
          : lower.includes("codex")
            ? "balanced"
            : "deep";
        return {
          id: slug,
          label: m.display_name ?? slug,
          aliases: [lower],
          tier,
        };
      }),
      fetchedAt,
    };
  } catch {
    return { models: [], fetchedAt: null };
  }
}

/**
 * Merge two `ModelEntry` lists. Operator overrides come first (their
 * label / tier metadata wins) and any registry entries that aren't
 * already covered are appended after. Dedup is by `id`,
 * case-insensitive — so an override for `"GPT-5.5"` masks the cache's
 * `"gpt-5.5"` row instead of double-listing the same model.
 */
export function mergeModelLists(
  overrides: ModelEntry[],
  registry: ModelEntry[],
): ModelEntry[] {
  const seen = new Set<string>();
  const out: ModelEntry[] = [];
  for (const m of overrides) {
    const key = m.id.toLowerCase();
    if (seen.has(key)) continue;
    seen.add(key);
    out.push(m);
  }
  for (const m of registry) {
    const key = m.id.toLowerCase();
    if (seen.has(key)) continue;
    seen.add(key);
    out.push(m);
  }
  return out;
}

/**
 * Resolve a free-form model nickname (`"opus"`, `"gpt-5.5"`, the full
 * id) against the registry. Falls back to the literal input when no
 * registry match — that lets the user pass a model id the registry
 * doesn't know about (a brand-new release, a private fine-tune, etc.).
 */
export function resolveModelInRegistry(
  raw: string,
  agent: "claude" | "codex",
  registry: ModelRegistry,
): string {
  const k = raw.trim();
  if (!k) return "";
  const list = registry[agent] ?? [];
  const lower = k.toLowerCase();
  // Exact id match.
  for (const m of list) if (m.id === k) return m.id;
  // Alias / case-insensitive id match.
  for (const m of list) {
    if (m.id.toLowerCase() === lower) return m.id;
    if (m.aliases.some((a) => a.toLowerCase() === lower)) return m.id;
  }
  // Substring match on aliases — last resort, helps loose nicknames
  // resolve even when whitespace or punctuation is weird.
  for (const m of list) {
    if (m.aliases.some((a) => a.toLowerCase().replace(/\s+/g, "") === lower.replace(/\s+/g, ""))) {
      return m.id;
    }
  }
  return k;
}

